package model;

public class Parrafo {

	String contenido;

	public Parrafo() {
	}

	public Parrafo(String contenido) {
		super();
		this.contenido = contenido;
	}

	public String getContenido() {
		return contenido;
	}

	public void setContenido(String contenido) {
		this.contenido = contenido;
	}

}
